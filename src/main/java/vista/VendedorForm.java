    private void cargarRutas() {
        cbRutas.removeAllItems(); // Limpiar opciones
        cbRutas.addItem("Todas"); // Opción para mostrar todo

        List<Ruta> rutas = rutaDAO.obtenerRutas();
        for (Ruta ruta : rutas) {
            cbRutas.addItem(ruta.getRUTNOM()); // Mostrar el destino en el ComboBox
        }
    }

    private void cargarViajes() {
        modeloTabla.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos
        String seleccion = (String) cbRutas.getSelectedItem();
        List<Viaje> viajes;

        if ("Todas".equals(seleccion)) {
            viajes = viajeDAO.obtenerTodos();
        } else {
            String codRuta = rutaDAO.obtenerCodigoPorNombre(seleccion);
            viajes = viajeDAO.obtenerPorRuta(codRuta);
        }

        for (Viaje v : viajes) {
            modeloTabla.addRow(new Object[]{
                v.getVIANRO(),
                v.getVIAFCH(),
                v.getVIAHRS(),
                v.getBUSNRO()
            });
        }
    } 
    
    private void cargarImagenRuta(String destino) {
        String rutaImagen = "src/main/java/img/turismo/" + destino + ".jpg"; // Ruta de la imagen
        File archivo = new File(rutaImagen);
        
        System.out.println("Ruta imagen: " + archivo.getAbsolutePath());

        if (archivo.exists()) {
            ImageIcon icono = new ImageIcon(rutaImagen);
            Image imagen = icono.getImage().getScaledInstance(200, 200, Image.SCALE_SMOOTH); // Ajustar tamaño
            lblImagenRuta.setIcon(new ImageIcon(imagen));
        } else {
            lblImagenRuta.setIcon(null); // Limpiar si no hay imagen
        }
    }
    
    
    
