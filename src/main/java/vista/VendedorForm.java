    String idRutaSeleccionada;
    private void tablaViajesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaViajesMouseClicked
        if (evt.getClickCount() == 1) {
            int filaSeleccionada = tablaViajes.getSelectedRow();

            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona una ruta válida", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Object valor = tablaViajes.getValueAt(filaSeleccionada, 0);

            if (valor != null) {
                idRutaSeleccionada = valor.toString(); 
                VendedorFormVenta formvendedor = new VendedorFormVenta(idRutaSeleccionada);
                formvendedor.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo obtener el ID de la ruta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_tablaViajesMouseClicked

